#include <bits/stdc++.h>
using namespace std;
int main()
{
    int number;
    cout << "Enter the Number : " << endl;
    cin >> number;

    if (number % 2 == 0)
    {
        cout << "Number Is Even !! " << endl;
    }
    else
    {
        cout << "Number Is Odd !! " << endl;
    }
    return 0;
}